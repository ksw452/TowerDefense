


[System.Serializable]
public class AtkInfo
{
    public int atk;
    public float atkSpeed;
    public float atkRange;
    public int cost;


    public AtkInfo(int a, float aS, float aR,int c)
        {

        atk = a;
        atkSpeed = aS;
        atkRange = aR;
        cost = c;

        }
}
